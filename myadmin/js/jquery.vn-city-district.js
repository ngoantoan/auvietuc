(function($){
    $.fn.extend({
        //plugin name - vnCityDistrict  
        vnCityDistrict: function(options){

            //Settings list and the default values  
            var defaults = {
                selectCity: 'selCity',
                selectDistrict: 'selDistrict',
				selectedCityValue: '',
				selectedDistrictValue: '',
				objJson: vnCityDistrictJson
            };
            
            var options = $.extend(defaults, options);
			
            return this.each(function(){
				var o = options;  
	            var obj = $(this);
				
				
				var _renderCityOption = function(){
					var $return = '';
					$(o.objJson).each(function(){
						if (o.selectedCityValue != '' && this.value == o.selectedCityValue) {
							$return += '<option value="'+this.value+' " selected>'+this.name+'</option>';
						} else {
							$return += '<option value="'+this.value+'">'+this.name+'</option>';	
						}
						
					});
					return $return;
				}
	        	
				var _renderDistrictOption = function(valueOfCity){
					var $return = '';
					$(o.objJson).each(function(){				
						if ($.trim(this.value) == $.trim(valueOfCity)) {
							var district = this.district;
							$(district).each(function(){
								if (o.selectedDistrictValue != '' && this.value == o.selectedDistrictValue) {
									$return += '<option value="' + this.value + ' " selected>' + this.name + '</option>';
								} else {
									$return += '<option value="' + this.value + '">' + this.name + '</option>';
								}
							});
						}
					});
					return $return;
				}
				
//				var _getCityValue = function(index){
//					return o.objJson[index].value;
//				}
				
				var _run = function(){
					var selCity = $('#'+o.selectCity, obj);  
					var selDistrict = $('#'+o.selectDistrict, obj);  
					if (selCity == undefined) {
						alert('The input type select of City has not been set');
						return false;
					}
					if (selDistrict == undefined) {
						alert('The input type select of District has not been set');
						return false;
					}
					$(selCity).html(_renderCityOption());
					var cityVal = $(selCity).val();
					
					$(selDistrict).html(_renderDistrictOption(cityVal));
					$(selCity).change(function(){
						$(selDistrict).html(_renderDistrictOption(this.value));
					});
					
				};
				
                _run();
            });
        }
    });
})(jQuery);
